package LogicaDeProgramacao.Arrays;

public class Arrays2 {
    public static void main(String[] args) {
        String[] nomes = new String[4];

        nomes[0] = "Igor";
        nomes[1] = "Odacir";
        nomes[2] = "Eliane";
        nomes[3] = "Vanessa";

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }
    }
}
